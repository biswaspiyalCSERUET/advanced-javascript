const first=2;
const second="2";

//double equal means comparing the value only
if(first==second){
    console.log("Condition is true");
}
else{
    console.log("condition is false")
}

//triple equal means comparing both the values and the types
if(first===second){
    console.log("Condition is true");
}
else{
    console.log("condition is false")
}

//0 means false
//1 means true